"""#Outliers Detection"""

def remove_outlier(df,column):
  Q1=df[column].quantile(0.25)
  Q3=df[column].quantile(0.75)
  IQR=Q3-Q1
  Lower_bound=Q1-1.5*IQR
  upper_bound=Q3+1.5*IQR
  return(df[(df[column]>=Lower_bound)&(df[column]<=upper_bound)])

for col in df.columns:
  df_iqr=remove_outlier(df,col)

"""Comment: Here we get clean data without outliers"""

print("Before outlier removal shape:",df.shape)
print("After outlier removal shape:",df_iqr.shape)

"""Comment: As both dataframe have same shape.There are no outliers in a dataset"""

